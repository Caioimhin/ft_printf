#include "ft_printf.h"

void	ft_display_str(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] != '%' || (str[i] == '%' && str[i + 1] == '%'))
		{
			if (str[i] == '%')
				i++;
			ft_putchar(str[i]);
		}
		i++;
	}
}
